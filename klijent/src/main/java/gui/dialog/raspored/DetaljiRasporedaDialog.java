    private void popuniTabelu() {

        List<Angazovanje> angazovanja = new ArrayList<>();
        angazovanja = AngazovanjeKontroler.getInstanca().ucitajListuAngazovanja(angazovanja);

        List<Angazovanje> filter = new ArrayList<>();
        for (Angazovanje a : angazovanja) {
            if (a.getRaspored().getDatum().equals(raspored.getDatum())) {
                filter.add(a);
            }
        }

        AngazovanjeTableModel atm = new AngazovanjeTableModel(filter);
        tblAngazovanja.setModel(atm);
    }
